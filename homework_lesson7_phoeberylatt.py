# Definitions for terms bellow

# Working directory = This is a single checkout of one version of the project. Generally the 'Umbrella Term' fr all your files and folders.
# Staging Area = Files that are going to be a part of teh nect commit. THis lets git know what changes in the file are going to occour next. #
# Local Repo (head) = this is the one on which we will make local changes on the computer.
# Remote repo (master) = This is a git repository that is on the internet or another network.

# Staged = this allows to continue making changes to the working directory and allows you to make small changes.
# Modified = As you edit files git sees them as modifed since youve changed them from your last commit.
# Committed = This is a snapshot of the repository at spesific times.

# git add = THis adds new or changed files in your working directory to the git staging area.
# git commit = This creates a commit which is like a snapshot of your repository.
# git push = This is used to upload local repository content to a remote repository.
# git fetch = This command downloads commits and other files from a remote repository.
# git merge = this takes the independent lines of development created by git branch and intergrates them into a single branch.
# git pull = this is used to fetch and download content from a remote respository and update the local one.
